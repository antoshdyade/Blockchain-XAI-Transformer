def evaluate_model(
        model,
        data,
        model_name):

    model.eval()

    with torch.no_grad():

        logits=model(data.x,data.edge_index)

        probs=F.softmax(logits,dim=1)

        pred=probs.argmax(dim=1)

    y_true=data.y[data.test_mask].cpu().numpy()

    y_pred=pred[data.test_mask].cpu().numpy()

    y_prob=probs[data.test_mask][:,1].cpu().numpy()

    acc=accuracy_score(y_true,y_pred)

    prec=precision_score(
        y_true,
        y_pred,
        zero_division=0
    )

    rec=recall_score(
        y_true,
        y_pred,
        zero_division=0
    )

    f1=f1_score(
        y_true,
        y_pred,
        zero_division=0
    )

    roc=roc_auc_score(
        y_true,
        y_prob
    )

    pr=average_precision_score(
        y_true,
        y_prob
    )

    results.append([
        model_name,
        acc,
        prec,
        rec,
        f1,
        roc,
        pr
    ])

    print("\n",model_name)

    print(classification_report(
        y_true,
        y_pred
    ))

    return y_true,y_pred,y_prob

class GCN(torch.nn.Module):

    def __init__(self,
                 in_channels,
                 hidden=128):

        super().__init__()

        self.conv1=GCNConv(
            in_channels,
            hidden
        )

        self.conv2=GCNConv(
            hidden,
            2
        )

    def forward(
        self,
        x,
        edge_index
    ):

        x=self.conv1(
            x,
            edge_index
        )

        x=F.relu(x)

        x=F.dropout(
            x,
            p=0.3,
            training=self.training
        )

        x=self.conv2(
            x,
            edge_index
        )

        return x

def train_gnn(
        model,
        data,
        epochs=100,
        lr=0.001):

    optimizer=torch.optim.Adam(
        model.parameters(),
        lr=lr,
        weight_decay=5e-4
    )

    criterion=torch.nn.CrossEntropyLoss()

    losses=[]

    for epoch in range(epochs):

        model.train()

        optimizer.zero_grad()

        out=model(
            data.x,
            data.edge_index
        )

        loss=criterion(
            out[data.train_mask],
            data.y[data.train_mask]
        )

        loss.backward()

        optimizer.step()

        losses.append(loss.item())

        if epoch%10==0:

            print(
                f"Epoch {epoch} Loss {loss.item():.4f}"
            )

    return losses

