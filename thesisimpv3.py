class GAT(torch.nn.Module):

    def __init__(
            self,
            in_channels):

        super().__init__()

        self.gat1=GATConv(
            in_channels,
            64,
            heads=4
        )

        self.gat2=GATConv(
            64*4,
            2,
            heads=1
        )

    def forward(
            self,
            x,
            edge_index):

        x=self.gat1(
            x,
            edge_index
        )

        x=F.elu(x)

        x=F.dropout(
            x,
            p=0.3,
            training=self.training
        )

        x=self.gat2(
            x,
            edge_index
        )

        return x

